function getStoredLocale() {
  const direct = localStorage.getItem("locale");
  if (direct === "zh-CN" || direct === "en") {
    return direct;
  }

  const persisted = localStorage.getItem("agent-studio-app");
  if (!persisted) {
    return "zh-CN";
  }

  try {
    const parsed = JSON.parse(persisted);
    const locale = parsed?.state?.locale;
    return locale === "en" || locale === "zh-CN" ? locale : "zh-CN";
  } catch {
    return "zh-CN";
  }
}

